#include <stdio.h>


/**
* main - checks the code
* _FILE_ - checks for uppercase character
* Return: 1 if c is uppercase, 0 otherwise
*
*/

int main() {
    /* The __FILE__ macro is a predefined macro in the C standard library
       that contains the current file's name. */
    printf("%s\n", __FILE__);
    /* return 0 to indicate successful execution of the program */
    return 0;
}
